wb = Workbook()
sheet1 = wb.add_sheet('Sheet 1')
sheet1.write(0, 0, 'Tweet')
sheet1.write(0, 1, 'Fecha')
sheet1.write(0, 2, 'Dia')
sheet1.write(0, 3, 'Retweets')
